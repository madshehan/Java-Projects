    private void passwordFieldFocusGained(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_passwordFieldFocusGained
      
        if(usernameField.getText().contains(" ")){
            
            passwordField.setEnabled(false);
            JOptionPane.showMessageDialog(this, "<html><body><h4>Username Cannot Contain Whitespace Characters !<br><br><h4><center>Please Try Again.....</center></h4></body></html>.","Error !" ,JOptionPane.ERROR_MESSAGE);
            
        }
        else if(usernameField.getText().isEmpty()){
            
            passwordField.setEnabled(false);
            JOptionPane.showMessageDialog(this, "<html><h3>Username Cannot Be Empty !</h3></html>","Warning !" ,JOptionPane.WARNING_MESSAGE);
            
        }

    }//GEN-LAST:event_passwordFieldFocusGained

    private void passwordFieldKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_passwordFieldKeyTyped

        passVisibility.setEnabled(true);
        passVisibility.setIcon(new ImageIcon(getClass().getResource("Webp.net-resizeimage (1).png")));
        
        if(evt.getKeyChar() == KeyEvent.VK_BACK_SPACE || evt.getKeyChar()== KeyEvent.VK_DELETE){
            
            if(passwordField.getPassword().length==0){
                
                passVisibility.setIcon(new ImageIcon(getClass().getResource("Webp.net-resizeimage (2).png"))); 
                passVisibility.setEnabled(false);
            }
        }
        
        if(evt.getKeyChar() == KeyEvent.VK_ENTER){
            
                LogIn.doClick();
                
        }
    }//GEN-LAST:event_passwordFieldKeyTyped

