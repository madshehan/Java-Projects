    private void LogInActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_LogInActionPerformed
        
        passwordField.setEchoChar((char) 0);
        String pass = new String (passwordField.getPassword());
        passwordField.setEchoChar('*');
        
        try{
            
            Class.forName("com.mysql.cj.jdbc.Driver");
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost/Sri_Nandana","root","iammad_287");
            Statement stmt = (Statement)con.createStatement();
            
             ResultSet result = stmt.executeQuery("SELECT username,password FROM Admin;");
             result.next();

             if(result.getString("Username").equals(usernameField.getText()) && result.getString("Password").equals(pass)){
                 
                 JOptionPane.showMessageDialog(null, "<html><h4>Login Successful !</h4></html>");
                 //Billing bl = new Billing();
                // bl.setVisible(true);
                 dispose();
             }
             
             else{
                 
                 JOptionPane.showMessageDialog(this, "<html><h4>Username or Password is Incorrect !<br><br><center>Please Try Again..</center></h4></html>","Error !",JOptionPane.ERROR_MESSAGE);
             }
             
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(this, "<html><h4>You Need To Create an Account First !</h4></html>", "Error !",JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_LogInActionPerformed

